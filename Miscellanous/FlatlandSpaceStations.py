# Complete the flatlandSpaceStations function below.
def flatlandSpaceStations(n, c):
    c.sort()
    r = list()
    r.append(c[0])
    r.append(n-1-c[len(c)-1])
    for i in range(1,len(c)):
        r.append((c[i]-c[i-1])//2)

    return max(r)


